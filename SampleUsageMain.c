void send_str(char* str, uint16_t len)
{
	uint16_t i;
	if (uart_empty == 1)
	{
		for(i= 0; i< len; i++)
		{
			uart_buffer[i] = str[i];
		}
		HAL_UART_Transmit_DMA(&huart2, uart_buffer, len);
		uart_empty = 0;
	}
}

uint8_t isI2CReady(uint16_t DevAddress)
{
	HAL_StatusTypeDef result;
	result = HAL_I2C_IsDeviceReady(&hi2c1, (uint16_t) (DevAddress<<1), 2, 2);
	return (result == HAL_OK) ? 1 : 0;
}

